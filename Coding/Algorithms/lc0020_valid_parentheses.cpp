/*
 * 20. 有效的括号
 * 仅含 '(', ')', '{', '}', '[', ']' 的字符串，判断是否合法配对闭合。
 *
 * 解法：栈。左括号入栈；右括号则与栈顶匹配弹出；最后栈空则合法。
 * 时间 O(n)，空间 O(n)。
 *
 * 输入：一行字符串（无空格）。
 * 输出：YES 或 NO。
 */
#include <bits/stdc++.h>
using namespace std;

// ============================================================================
// 核心方法（题号 20）
// ============================================================================
bool isValid(string s) {
    stack<char> st;
    auto match = [](char a, char b) {
        return (a == '(' && b == ')') || (a == '[' && b == ']') || (a == '{' && b == '}');
    };
    for (char c : s) {
        if (c == '(' || c == '[' || c == '{')
            st.push(c);
        else {
            if (st.empty() || !match(st.top(), c)) return false;
            st.pop();
        }
    }
    return st.empty();
}

// ============================================================================
// ACM：读入 → 调用 → 输出
// ============================================================================
int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    string s;
    if (!(cin >> s)) return 0;
    cout << (isValid(s) ? "YES" : "NO") << '\n';
    return 0;
}
