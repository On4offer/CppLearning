#include <iostream>
using namespace std;

/*
C++14引入了二进制字面量和数字分隔符，方便在代码中表示二进制数和大数字。
二进制字面量以0b或0B开头，表示二进制数。数字分隔符使用单引号'，可以在数字中插入分隔符，提高可读性。
*/
int main() {
    // 二进制字面量
    // 二进制字面量是以0b或0B开头的整数常量，表示二进制数。
    // 用途是方便在二进制数中表示1和0，避免使用0和1表示二进制数。

    int a = 0b1010;  // 10
    int b = 0B1100;  // 12
    
    cout << "0b1010 = " << a << endl;
    cout << "0B1100 = " << b << endl;
    
    // 数字分隔符
    // 数字分隔符是使用单引号'，可以在数字中插入分隔符，提高可读性。
    int c = 1'000'000;  // 1000000
    cout << "1'000'000 = " << c << endl;
    
    return 0;
}